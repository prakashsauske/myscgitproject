package au.com.woolworths.portal.param;

import java.util.ArrayList;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

import au.com.woolworths.portal.model.SUGOReports;

@JsonIgnoreProperties(ignoreUnknown = true)
public class SUGOReportParam {

	@JsonProperty("fromDate")
	private String fromDate;
	
	@JsonProperty("storeNumber")
	private String storeNumber;
	
	@JsonProperty("msg")
	private String msg;
	
	@JsonProperty("iserror")
	private boolean iserror;
	
	@JsonProperty("orderReports")
	private ArrayList<SUGOReports> orderReports;
	
	@JsonProperty("deliveryDate")
	private String deliveryDate;
	
	@JsonProperty("reviewTime")
	private String reviewTime;
	
	@JsonProperty("rosterDate")
	private String rosterDate;
	
	private String pageNo;
	
	public String getPageNo() {
		return pageNo;
	}
	public void setPageNo(String pageNo) {
		this.pageNo = pageNo;
	}
	public ArrayList<SUGOReports> getOrderReports() {
		return orderReports;
	}
	public void setOrderReports(ArrayList<SUGOReports> orderReports) {
		this.orderReports = orderReports;
	}
	/**
	 * @return the iserror
	 */
	public boolean isIserror() {
		return iserror;
	}
	/**
	 * @param iserror the iserror to set
	 */
	public void setIserror(boolean iserror) {
		this.iserror = iserror;
	}
	/**
	 * @return the fromDate
	 */
	public String getFromDate() {
		return fromDate;
	}
	/**
	 * @param fromDate the fromDate to set
	 */
	public void setFromDate(String fromDate) {
		this.fromDate = fromDate;
	}
	/**
	 * @return the storeNumber
	 */
	public String getStoreNumber() {
		return storeNumber;
	}
	/**
	 * @param storeNumber the storeNumber to set
	 */
	public void setStoreNumber(String storeNumber) {
		this.storeNumber = storeNumber;
	}
	/**
	 * @return the msg
	 */
	public String getMsg() {
		return msg;
	}
	/**
	 * @param msg the msg to set
	 */
	public void setMsg(String msg) {
		this.msg = msg;
	}
	/**
	 * @return the deliveryDate
	 */
	public String getDeliveryDate() {
		return deliveryDate;
	}
	/**
	 * @param deliveryDate the deliveryDate to set
	 */
	public void setDeliveryDate(String deliveryDate) {
		this.deliveryDate = deliveryDate;
	}
	/**
	 * @return the reviewTime
	 */
	public String getReviewTime() {
		return reviewTime;
	}
	/**
	 * @param reviewTime the reviewTime to set
	 */
	public void setReviewTime(String reviewTime) {
		this.reviewTime = reviewTime;
	}
	/**
	 * @return the rosterDate
	 */
	public String getRosterDate() {
		return rosterDate;
	}
	/**
	 * @param rosterDate the rosterDate to set
	 */
	public void setRosterDate(String rosterDate) {
		this.rosterDate = rosterDate;
	}
	
	
	
	
	
}
