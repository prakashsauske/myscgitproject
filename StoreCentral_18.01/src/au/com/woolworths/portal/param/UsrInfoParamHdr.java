package au.com.woolworths.portal.param;



import java.util.ArrayList;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;


@JsonIgnoreProperties(ignoreUnknown = true)
public class UsrInfoParamHdr {

	@JsonProperty("msg")
	private String msg;

	@JsonProperty("typ")
	private String typ;

	@JsonProperty("passwordList")
	private ArrayList<UsrInfoParam> passwordList;

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public String getTyp() {
		return typ;
	}

	public void setTyp(String typ) {
		this.typ = typ;
	}

	public ArrayList<UsrInfoParam> getPasswordList() {
		return passwordList;
	}

	public void setPasswordList(ArrayList<UsrInfoParam> passwordList) {
		this.passwordList = passwordList;
	}
	

}
