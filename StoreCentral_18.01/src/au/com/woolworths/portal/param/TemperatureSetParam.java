package au.com.woolworths.portal.param;

public class TemperatureSetParam {

	private String purOrdNo;
	private String siteNo;
	private String grTemp1;
	private String grTemp2;

	public String getPurOrdNo() {
		return purOrdNo;
	}

	public void setPurOrdNo(String purOrdNo) {
		this.purOrdNo = purOrdNo;
	}

	public String getSiteNo() {
		return siteNo;
	}

	public void setSiteNo(String siteNo) {
		this.siteNo = siteNo;
	}

	public String getGrTemp1() {
		return grTemp1;
	}

	public void setGrTemp1(String grTemp1) {
		this.grTemp1 = grTemp1;
	}

	public String getGrTemp2() {
		return grTemp2;
	}

	public void setGrTemp2(String grTemp2) {
		this.grTemp2 = grTemp2;
	}

}
