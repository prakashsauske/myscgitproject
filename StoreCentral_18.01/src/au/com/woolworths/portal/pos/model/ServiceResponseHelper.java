package au.com.woolworths.portal.pos.model;

import java.util.List;

import org.codehaus.jackson.annotate.JsonProperty;

/**
 * @author 
 * @param <T>
 *
 */
public class ServiceResponseHelper<T> {

	@JsonProperty("results")
	private List<T> beanDtl;

	/**
	 * @return the beanDtl
	 */
	public List<T> getBeanDtl() {
		return beanDtl;
	}

	/**
	 * @param beanDtl the beanDtl to set
	 */
	public void setBeanDtl(List<T> beanDtl) {
		this.beanDtl = beanDtl;
	}

	
	
	

}
