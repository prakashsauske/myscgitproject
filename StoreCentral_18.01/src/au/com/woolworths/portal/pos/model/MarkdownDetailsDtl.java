	public void aggregate(MarkdownDetailsDtl tmpDept) {
		Double tmpAmt = getMarkdownPrice(tmpDept.getPriceOverrideRTC());
		Double cumAmt = getMarkdownPrice(this.getPriceOverrideRTC());
		cumAmt = cumAmt + tmpAmt;
		this.setPriceOverrideRTC(cumAmt.toString());

		tmpAmt = getMarkdownPrice(tmpDept.getClearance());
		cumAmt = getMarkdownPrice(this.getClearance());
		cumAmt = cumAmt + tmpAmt;
		this.setClearance(cumAmt.toString());
		
		tmpAmt = getMarkdownPrice(tmpDept.getAdvertisements());
		cumAmt = getMarkdownPrice(this.getAdvertisements());
		cumAmt = cumAmt + tmpAmt;
		this.setAdvertisements(cumAmt.toString());

		tmpAmt = getMarkdownPrice(tmpDept.getScanningPolicy());
		cumAmt = getMarkdownPrice(this.getScanningPolicy());
		cumAmt = cumAmt + tmpAmt;
		this.setScanningPolicy(cumAmt.toString());
		
		tmpAmt = getMarkdownPrice(tmpDept.getComp());
		cumAmt = getMarkdownPrice(this.getComp());
		cumAmt = cumAmt + tmpAmt;
		this.setComp(cumAmt.toString());
		
		
		tmpAmt = getMarkdownPrice(tmpDept.getSplActivity());
		cumAmt = getMarkdownPrice(this.getSplActivity());
		cumAmt = cumAmt + tmpAmt;
		this.setSplActivity(cumAmt.toString());

		
		tmpAmt = getMarkdownPrice(tmpDept.getStaffDiscount());
		cumAmt = getMarkdownPrice(this.getStaffDiscount());
		cumAmt = cumAmt + tmpAmt;
		this.setStaffDiscount(cumAmt.toString());

		tmpAmt = getMarkdownPrice(tmpDept.getLoyalty());
		cumAmt = getMarkdownPrice(this.getLoyalty());
		cumAmt = cumAmt + tmpAmt;
		this.setLoyalty(cumAmt.toString());

		tmpAmt = getMarkdownPrice(tmpDept.getPromotions());
		cumAmt = getMarkdownPrice(this.getPromotions());
		cumAmt = cumAmt + tmpAmt;
		this.setPromotions(cumAmt.toString());

		tmpAmt = getMarkdownPrice(tmpDept.getDeferedLoyalty());
		cumAmt = getMarkdownPrice(this.getDeferedLoyalty());
		cumAmt = cumAmt + tmpAmt;
		this.setDeferedLoyalty(cumAmt.toString());
	}
	
	private Double getMarkdownPrice(String amt) {
		if(CommonUtils.isNumeric(amt)) {
			return Double.parseDouble(amt);
		}
		return 0.0;
	}
	
