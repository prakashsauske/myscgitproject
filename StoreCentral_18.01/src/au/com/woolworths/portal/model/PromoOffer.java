package au.com.woolworths.portal.model;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class PromoOffer {
	@JsonProperty("offerStartDate")
	private String offerStartDate;

	@JsonProperty("offerEndDate")
	private String offerEndDate;

	@JsonProperty("promoInfo")
	private String promoInfo;

	@JsonProperty("weekStartDate")
	private String weekStartDate;

	@JsonProperty("msg")
	private String msg;
	
	@JsonProperty("article")
	private String article;
	
	@JsonProperty("uom")
	private String uom;

	public String getOfferStartDate() {
		return offerStartDate;
	}

	public void setOfferStartDate(String offerStartDate) {
		this.offerStartDate = offerStartDate;
	}

	public String getOfferEndDate() {
		return offerEndDate;
	}

	public void setOfferEndDate(String offerEndDate) {
		this.offerEndDate = offerEndDate;
	}

	public String getPromoInfo() {
		return promoInfo;
	}

	public void setPromoInfo(String promoInfo) {
		this.promoInfo = promoInfo;
	}

	public String getWeekStartDate() {
		return weekStartDate;
	}

	public void setWeekStartDate(String weekStartDate) {
		this.weekStartDate = weekStartDate;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public String getArticle() {
		return article;
	}

	public void setArticle(String article) {
		this.article = article;
	}

	public String getUom() {
		return uom;
	}

	public void setUom(String uom) {
		this.uom = uom;
	}

}
