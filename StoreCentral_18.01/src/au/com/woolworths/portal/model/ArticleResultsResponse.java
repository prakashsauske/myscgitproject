package au.com.woolworths.portal.model;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class ArticleResultsResponse {

	@JsonProperty("d")
	private ArticleResultsResponseHelper articleResultsResponseHelper;

	@JsonProperty("d")
	public ArticleResultsResponseHelper getArticleResultsResponseHelper() {
		return articleResultsResponseHelper;
	}

	@JsonProperty("d")
	public void setArticleResultsResponseHelper(
			ArticleResultsResponseHelper articleResultsResponseHelper) {
		this.articleResultsResponseHelper = articleResultsResponseHelper;
	}

}
