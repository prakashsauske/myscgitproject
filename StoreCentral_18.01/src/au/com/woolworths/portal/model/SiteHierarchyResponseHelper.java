package au.com.woolworths.portal.model;

import java.util.ArrayList;
import java.util.List;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class SiteHierarchyResponseHelper {

	@JsonProperty("results")
	private List<SiteHierarchy> SiteHierarchy;

	public List<SiteHierarchy> getSiteHierarchyList() {
		return SiteHierarchy;
	}

	public void setSiteHierarchyList(List<SiteHierarchy> siteHierarchyList) {
		SiteHierarchy = siteHierarchyList;
	}
}
