package au.com.woolworths.portal.model;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class ArticleProductCategory {

	@JsonProperty("article")
	private String articleNo;
	@JsonProperty("internal_id")
	private String internalId;
	@JsonProperty("prod_cat_description")
	private String prodCatDescription;
	@JsonProperty("prod_cat_hier_id")
	private String prodCatHierId;
	@JsonProperty("prod_cat_hier_des")
	private String prodCatHierDes;
	@JsonProperty("prod_cat_hier_ty1")
	private String prodCatHierty1;
	@JsonProperty("prod_cat_hier_typ")
	private String prodCatHierTyp;

	public String getProdCatHierTyp() {
		return prodCatHierTyp;
	}

	public void setProdCatHierTyp(String prodCatHierTyp) {
		this.prodCatHierTyp = prodCatHierTyp;
	}

	public String getInternalId() {
		return internalId;
	}

	public void setInternalId(String internalId) {
		this.internalId = internalId;
	}

	public String getProdCatDescription() {
		return prodCatDescription;
	}

	public void setProdCatDescription(String prodCatDescription) {
		this.prodCatDescription = prodCatDescription;
	}

	public String getProdCatHierId() {
		return prodCatHierId;
	}

	public void setProdCatHierId(String prodCatHierId) {
		this.prodCatHierId = prodCatHierId;
	}

	public String getProdCatHierDes() {
		return prodCatHierDes;
	}

	public void setProdCatHierDes(String prodCatHierDes) {
		this.prodCatHierDes = prodCatHierDes;
	}

	public String getProdCatHierty1() {
		return prodCatHierty1;
	}

	public void setProdCatHierty1(String prodCatHierty1) {
		this.prodCatHierty1 = prodCatHierty1;
	}

	public String getArticleNo() {
		return articleNo;
	}

	public void setArticleNo(String articleNo) {
		this.articleNo = articleNo;
	}

}