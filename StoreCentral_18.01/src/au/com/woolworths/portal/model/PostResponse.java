package au.com.woolworths.portal.model;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

/**
 * @author xrca4
 * 
 */
@JsonIgnoreProperties(ignoreUnknown = true)
public class PostResponse {
	@JsonProperty("d")
	private PostResponseHelper PostResponseHelper;

	@JsonProperty("d")
	public PostResponseHelper getPostResponseHelper() {
		return PostResponseHelper;
	}

	@JsonProperty("d")
	public void setPostResponseHelper(PostResponseHelper postResponseHelper) {
		PostResponseHelper = postResponseHelper;
	}

}
