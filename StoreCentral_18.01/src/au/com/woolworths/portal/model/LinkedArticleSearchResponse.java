package au.com.woolworths.portal.model;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class LinkedArticleSearchResponse {

	@JsonProperty("d")
	private LinkedArticleResponseHelper linkedArticleResponseHelper;

	/**
	 * @return the linkedArticleResponseHelper
	 */
	public LinkedArticleResponseHelper getLinkedArticleResponseHelper() {
		return linkedArticleResponseHelper;
	}

	/**
	 * @param linkedArticleResponseHelper
	 *            the linkedArticleResponseHelper to set
	 */
	public void setLinkedArticleResponseHelper(
			LinkedArticleResponseHelper linkedArticleResponseHelper) {
		this.linkedArticleResponseHelper = linkedArticleResponseHelper;
	}

}
