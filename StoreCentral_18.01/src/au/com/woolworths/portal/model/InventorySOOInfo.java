package au.com.woolworths.portal.model;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class InventorySOOInfo {

	@JsonProperty("meins")
	private String baseUnitofMeasure;
	@JsonProperty("on_order")
	private String stockOnOrder;
	@JsonProperty("werks")
	private String siteNo;
	@JsonProperty("matnr")
	private String articleNumber;
	@JsonProperty("lgort")
	private String storageLocation;
	@JsonProperty("on_order_consign")
	private String onOrderConsign;
	@JsonProperty("gr_blocked")
	private String grBlockedStock;
	@JsonProperty("trspt_order")
	private String transportOrders;
	@JsonProperty("transit_cc")
	private String transitCrossCompany;
	@JsonProperty("gen_matnr")
	private String crossSiteConfigurableArticleGen;
	@JsonProperty("aggr_level")
	private String aggrLevel;
	@JsonProperty("struct_matnr")
	private String crossSiteConfigurableArticleStruct;
	@JsonProperty("source_erp_pos")
	private String sourceErpPos;

	public String getBaseUnitofMeasure() {
		return baseUnitofMeasure;
	}

	public void setBaseUnitofMeasure(String baseUnitofMeasure) {
		this.baseUnitofMeasure = baseUnitofMeasure;
	}

	public String getCrossSiteConfigurableArticleStruct() {
		return crossSiteConfigurableArticleStruct;
	}

	public void setCrossSiteConfigurableArticleStruct(
			String crossSiteConfigurableArticleStruct) {
		this.crossSiteConfigurableArticleStruct = crossSiteConfigurableArticleStruct;
	}

	public String getCrossSiteConfigurableArticleGen() {
		return crossSiteConfigurableArticleGen;
	}

	public void setCrossSiteConfigurableArticleGen(
			String crossSiteConfigurableArticleGen) {
		this.crossSiteConfigurableArticleGen = crossSiteConfigurableArticleGen;
	}

	public String getSourceErpPos() {
		return sourceErpPos;
	}

	public void setSourceErpPos(String sourceErpPos) {
		this.sourceErpPos = sourceErpPos;
	}

	public String getTransitCrossCompany() {
		return transitCrossCompany;
	}

	public void setTransitCrossCompany(String transitCrossCompany) {
		this.transitCrossCompany = transitCrossCompany;
	}

	public String getTransportOrders() {
		return transportOrders;
	}

	public void setTransportOrders(String transportOrders) {
		this.transportOrders = transportOrders;
	}

	public String getAggrLevel() {
		return aggrLevel;
	}

	public void setAggrLevel(String aggrLevel) {
		this.aggrLevel = aggrLevel;
	}

	public String getGrBlockedStock() {
		return grBlockedStock;
	}

	public void setGrBlockedStock(String grBlockedStock) {
		this.grBlockedStock = grBlockedStock;
	}

	public String getOnOrderConsign() {
		return onOrderConsign;
	}

	public void setOnOrderConsign(String onOrderConsign) {
		this.onOrderConsign = onOrderConsign;
	}

	public String getStorageLocation() {
		return storageLocation;
	}

	public void setStorageLocation(String storageLocation) {
		this.storageLocation = storageLocation;
	}

	public String getArticleNumber() {
		return articleNumber;
	}

	public void setArticleNumber(String articleNumber) {
		this.articleNumber = articleNumber;
	}

	public String getStockOnOrder() {
		return stockOnOrder;
	}

	public void setStockOnOrder(String stockOnOrder) {
		this.stockOnOrder = stockOnOrder;
	}

	public String getSiteNo() {
		return siteNo;
	}

	public void setSiteNo(String siteNo) {
		this.siteNo = siteNo;
	}

}
