package au.com.woolworths.portal.model;

import java.util.List;

import org.codehaus.jackson.annotate.JsonProperty;

public class ArticlePromotionServiceResponceHelper {

	@JsonProperty("results")
	private List<ArticlePromotionServiceInfo> PromotionServiceInfos;

	@JsonProperty("results")
	public List<ArticlePromotionServiceInfo> getPromotionServiceInfos() {
		return PromotionServiceInfos;
	}

	@JsonProperty("results")
	public void setPromotionServiceInfos(
			List<ArticlePromotionServiceInfo> promotionServiceInfos) {
		PromotionServiceInfos = promotionServiceInfos;
	}

}
