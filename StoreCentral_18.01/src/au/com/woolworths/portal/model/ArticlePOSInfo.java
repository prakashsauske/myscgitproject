package au.com.woolworths.portal.model;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class ArticlePOSInfo {

	@JsonProperty("posWeighed")
	private String posWeighed;

	@JsonProperty("pos_weighed")
	private String pos_weighed_t;

	@JsonProperty("prohibitPrcOverride")
	private String prohibitPrcOverride;
	@JsonProperty("prohibit_prc_override")
	private String prohibit_prc_override_t;

	@JsonProperty("defaultPrice")
	private String defaultPrice;
	@JsonProperty("default_price")
	private String default_price_t;

	@JsonProperty("msg")
	private String msg;

	@JsonProperty("forceQty")
	private String forceQty;
	@JsonProperty("force_qty")
	private String force_qty_t;

	@JsonProperty("posTareWeight")
	private String posTareWeight;
	@JsonProperty("pos_tare_weight")
	private String pos_tare_weight_t;

	@JsonProperty("inhibitQtyRepeat")
	private String inhibitQtyRepeat;
	
	@JsonProperty("inhibit_qty_repeat")
	private String inhibit_qty_repeat_t;

	@JsonProperty("manualPrice")
	private String manualPrice;
	
	@JsonProperty("manual_price")
	private String manualPrice_t;

	@JsonProperty("foodStamp")
	private String foodStamp;

	@JsonProperty("food_stamp")
	private String food_stamp_t;

	@JsonProperty("prohibitDiscount")
	private String prohibitDiscount;
	@JsonProperty("prohibit_discount")
	private String prohibit_discount_t;

	public ArticlePOSInfo() {

	}

	public ArticlePOSInfo(@JsonProperty("pos_weighed") String posWeighed,
			@JsonProperty("prohibit_prc_override") String prohibitPrcOverride,
			@JsonProperty("default_price") String defaultPrice,
			@JsonProperty("force_qty") String forceQty,
			@JsonProperty("pos_tare_weight") String posTareWeight,
			@JsonProperty("inhibit_qty_repeat") String inhibitQtyRepeat,
			@JsonProperty("manual_price") String manualPrice,
			@JsonProperty("food_stamp") String foodStamp,
			@JsonProperty("prohibit_discount") String prohibitDiscount) {
		super();
		this.posWeighed = posWeighed;
		this.pos_weighed_t = posWeighed;
		this.prohibitPrcOverride = prohibitPrcOverride;
		this.prohibit_prc_override_t = prohibitPrcOverride;
		this.defaultPrice = defaultPrice;
		this.default_price_t = defaultPrice;
		this.forceQty = forceQty;
		this.force_qty_t = forceQty;
		this.pos_tare_weight_t = posTareWeight;
		this.posTareWeight = posTareWeight;
		this.inhibitQtyRepeat = inhibitQtyRepeat;
		this.inhibit_qty_repeat_t = inhibitQtyRepeat;
		this.manualPrice = manualPrice;
		this.manualPrice_t = manualPrice;
		this.foodStamp = foodStamp;
		this.food_stamp_t = foodStamp;
		this.prohibitDiscount = prohibitDiscount;
		this.prohibit_discount_t = prohibitDiscount;
	}

	/**
	 * @return the posWeighed
	 */
	public String getPosWeighed() {
		return posWeighed;
	}

	/**
	 * @param posWeighed
	 *            the posWeighed to set
	 */
	public void setPosWeighed(String posWeighed) {
		this.posWeighed = posWeighed;
	}

	/**
	 * @return the prohibitPrcOverride
	 */
	public String getProhibitPrcOverride() {
		return prohibitPrcOverride;
	}

	/**
	 * @param prohibitPrcOverride
	 *            the prohibitPrcOverride to set
	 */
	public void setProhibitPrcOverride(String prohibitPrcOverride) {
		this.prohibitPrcOverride = prohibitPrcOverride;
	}

	/**
	 * @return the defaultPrice
	 */
	public String getDefaultPrice() {
		return defaultPrice;
	}

	/**
	 * @param defaultPrice
	 *            the defaultPrice to set
	 */
	public void setDefaultPrice(String defaultPrice) {
		this.defaultPrice = defaultPrice;
	}

	/**
	 * @return the msg
	 */
	public String getMsg() {
		return msg;
	}

	/**
	 * @param msg
	 *            the msg to set
	 */
	public void setMsg(String msg) {
		this.msg = msg;
	}

	/**
	 * @return the forceQty
	 */
	public String getForceQty() {
		return forceQty;
	}

	/**
	 * @param forceQty
	 *            the forceQty to set
	 */
	public void setForceQty(String forceQty) {
		this.forceQty = forceQty;
	}

	/**
	 * @return the posTareWeight
	 */
	public String getPosTareWeight() {
		return posTareWeight;
	}

	/**
	 * @param posTareWeight
	 *            the posTareWeight to set
	 */
	public void setPosTareWeight(String posTareWeight) {
		this.posTareWeight = posTareWeight;
	}

	/**
	 * @return the inhibitQtyRepeat
	 */
	public String getInhibitQtyRepeat() {
		return inhibitQtyRepeat;
	}

	/**
	 * @param inhibitQtyRepeat
	 *            the inhibitQtyRepeat to set
	 */
	public void setInhibitQtyRepeat(String inhibitQtyRepeat) {
		this.inhibitQtyRepeat = inhibitQtyRepeat;
	}

	/**
	 * @return the manualPrice
	 */
	public String getManualPrice() {
		return manualPrice;
	}

	/**
	 * @param manualPrice
	 *            the manualPrice to set
	 */
	public void setManualPrice(String manualPrice) {
		this.manualPrice = manualPrice;
	}

	/**
	 * @return the foodStamp
	 */
	public String getFoodStamp() {
		return foodStamp;
	}

	/**
	 * @param foodStamp
	 *            the foodStamp to set
	 */
	public void setFoodStamp(String foodStamp) {
		this.foodStamp = foodStamp;
	}

	/**
	 * @return the prohibitDiscount
	 */
	public String getProhibitDiscount() {
		return prohibitDiscount;
	}

	/**
	 * @param prohibitDiscount
	 *            the prohibitDiscount to set
	 */
	public void setProhibitDiscount(String prohibitDiscount) {
		this.prohibitDiscount = prohibitDiscount;
	}

}
