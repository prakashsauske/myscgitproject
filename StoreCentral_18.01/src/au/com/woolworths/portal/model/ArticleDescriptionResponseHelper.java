package au.com.woolworths.portal.model;

import java.util.List;

import org.codehaus.jackson.annotate.JsonProperty;

public class ArticleDescriptionResponseHelper {

	@JsonProperty("results")
	private List<ArticleDescription> articleDescriptionList;

	@JsonProperty("results")
	public List<ArticleDescription> getArticleDescriptionList() {
		return articleDescriptionList;
	}

	@JsonProperty("results")
	public void setArticleDescriptionList(
			List<ArticleDescription> articleDescriptionList) {
		this.articleDescriptionList = articleDescriptionList;
	}

}
