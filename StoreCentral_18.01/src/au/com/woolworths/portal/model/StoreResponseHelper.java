package au.com.woolworths.portal.model;

import java.util.List;

import org.codehaus.jackson.annotate.JsonProperty;

public class StoreResponseHelper {

	@JsonProperty("results")
	private List<Store> storeDetailsList;

	@JsonProperty("results")
	public List<Store> getstoreDetailsList() {
		return storeDetailsList;
	}

	@JsonProperty("results")
	public void setstoreDetailsList(List<Store> storeDetailsList) {
		this.storeDetailsList = storeDetailsList;
	}
}
