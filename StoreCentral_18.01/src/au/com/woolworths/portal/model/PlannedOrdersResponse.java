package au.com.woolworths.portal.model;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class PlannedOrdersResponse {

	@JsonProperty("d")
	private PlannedOrdersResponseHelper plannedOrdersResponseHelper;

	public PlannedOrdersResponseHelper getPlannedOrdersResponseHelper() {
		return plannedOrdersResponseHelper;
	}

	public void setPlannedOrdersResponseHelper(
			PlannedOrdersResponseHelper plannedOrdersResponseHelper) {
		this.plannedOrdersResponseHelper = plannedOrdersResponseHelper;
	}

}
