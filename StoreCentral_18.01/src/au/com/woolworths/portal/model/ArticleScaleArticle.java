package au.com.woolworths.portal.model;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class ArticleScaleArticle {

	@JsonProperty("article")
	private String article;
	@JsonProperty("site")
	private String site;
	@JsonProperty("uom")
	private String uom;
	@JsonProperty("scale_article")
	private String scaleArticle;
	@JsonProperty("forced_qty")
	private String forcedQty;
	@JsonProperty("forced_price")
	private String forcedPrice;
	@JsonProperty("tare_weight")
	private String tareWeight;

	public String getArticle() {
		return article;
	}

	public void setArticle(String article) {
		this.article = article;
	}

	public String getSite() {
		return site;
	}

	public void setSite(String site) {
		this.site = site;
	}

	public String getUom() {
		return uom;
	}

	public void setUom(String uom) {
		this.uom = uom;
	}

	public String getScaleArticle() {
		return scaleArticle;
	}

	public void setScaleArticle(String scaleArticle) {
		this.scaleArticle = scaleArticle;
	}

	public String getForcedQty() {
		return forcedQty;
	}

	public void setForcedQty(String forcedQty) {
		this.forcedQty = forcedQty;
	}

	public String getForcedPrice() {
		return forcedPrice;
	}

	public void setForcedPrice(String forcedPrice) {
		this.forcedPrice = forcedPrice;
	}

	public String getTareWeight() {
		return tareWeight;
	}

	public void setTareWeight(String tareWeight) {
		this.tareWeight = tareWeight;
	}

}
