package au.com.woolworths.portal.model;

import java.util.List;
import org.codehaus.jackson.annotate.JsonProperty;

public class SupplierModelResponseHelper {

	@JsonProperty("results")
	private List<SupplierModel> supplierModelList;

	@JsonProperty("results")
	public List<SupplierModel> getSupplierModelList() {
		return supplierModelList;
	}

	@JsonProperty("results")
	public void setSupplierModelList(List<SupplierModel> supplierModelList) {
		this.supplierModelList = supplierModelList;
	}

}
