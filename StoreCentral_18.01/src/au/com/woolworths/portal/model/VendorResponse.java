package au.com.woolworths.portal.model;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class VendorResponse {

	@JsonProperty("d")
	private VendorResponseHelper vendorResponseHelper;

	@JsonProperty("d")
	public VendorResponseHelper getVendorResponseHelper() {
		return vendorResponseHelper;
	}

	@JsonProperty("d")
	public void setVendorResponseHelper(
			VendorResponseHelper vendorResponseHelper) {
		this.vendorResponseHelper = vendorResponseHelper;
	}

}
