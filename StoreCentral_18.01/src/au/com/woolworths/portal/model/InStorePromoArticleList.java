package au.com.woolworths.portal.model;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class InStorePromoArticleList {
	
	@JsonProperty("IV_PROMO_TYPE")
	private String promoType;
	@JsonProperty("IV_ARTICLE")
	private String article;
	@JsonProperty("IV_SALE_FROM")
	private String salesFrom;
	@JsonProperty("IV_SALE_TO")
	private String salesTo;
	@JsonProperty("IV_SALES_QTY")
	private String salesQty;
	@JsonProperty("IV_SALES_PRICE")
	private String salesPrice;
	@JsonProperty("IV_SALES_PRICE_UNIT")
	private String salesPriceUnit;
	@JsonProperty("IV_SALES_UNIT")
	private String salesUnit;
	@JsonProperty("IV_DISPLAY_TYPE")
	private String displayType;
	@JsonProperty("MSG")
	private String msg;
	/**
	 * @return the promoType
	 */
	public String getPromoType() {
		return promoType;
	}
	/**
	 * @param promoType the promoType to set
	 */
	public void setPromoType(String promoType) {
		this.promoType = promoType;
	}
	/**
	 * @return the article
	 */
	public String getArticle() {
		return article;
	}
	/**
	 * @param article the article to set
	 */
	public void setArticle(String article) {
		this.article = article;
	}
	/**
	 * @return the salesFrom
	 */
	public String getSalesFrom() {
		return salesFrom;
	}
	/**
	 * @param salesFrom the salesFrom to set
	 */
	public void setSalesFrom(String salesFrom) {
		this.salesFrom = salesFrom;
	}
	
	/**
	 * @return the salesTo
	 */
	public String getSalesTo() {
		return salesTo;
	}
	/**
	 * @param salesTo the salesTo to set
	 */
	public void setSalesTo(String salesTo) {
		this.salesTo = salesTo;
	}
	/**
	 * @return the salesQty
	 */
	public String getSalesQty() {
		return salesQty;
	}
	/**
	 * @param salesQty the salesQty to set
	 */
	public void setSalesQty(String salesQty) {
		this.salesQty = salesQty;
	}
	/**
	 * @return the salesPrice
	 */
	public String getSalesPrice() {
		return salesPrice;
	}
	/**
	 * @param salesPrice the salesPrice to set
	 */
	public void setSalesPrice(String salesPrice) {
		this.salesPrice = salesPrice;
	}
	/**
	 * @return the salesPriceUnit
	 */
	public String getSalesPriceUnit() {
		return salesPriceUnit;
	}
	/**
	 * @param salesPriceUnit the salesPriceUnit to set
	 */
	public void setSalesPriceUnit(String salesPriceUnit) {
		this.salesPriceUnit = salesPriceUnit;
	}
	/**
	 * @return the salesUnit
	 */
	public String getSalesUnit() {
		return salesUnit;
	}
	/**
	 * @param salesUnit the salesUnit to set
	 */
	public void setSalesUnit(String salesUnit) {
		this.salesUnit = salesUnit;
	}
	/**
	 * @return the displayType
	 */
	public String getDisplayType() {
		return displayType;
	}
	/**
	 * @param displayType the displayType to set
	 */
	public void setDisplayType(String displayType) {
		this.displayType = displayType;
	}
	/**
	 * @return the msg
	 */
	public String getMsg() {
		return msg;
	}
	/**
	 * @param msg the msg to set
	 */
	public void setMsg(String msg) {
		this.msg = msg;
	}
	
	
}
