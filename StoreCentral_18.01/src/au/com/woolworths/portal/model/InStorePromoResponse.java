package au.com.woolworths.portal.model;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class InStorePromoResponse {
	
	@JsonProperty("d")
	private InStorePromoResponseList responseArticle;

	/**
	 * @return the responseArticle
	 */
	@JsonProperty("d")
	public InStorePromoResponseList getResponseArticle() {
		return responseArticle;
	}

	/**
	 * @param responseArticle the responseArticle to set
	 */
	@JsonProperty("d")
	public void setResponseArticle(InStorePromoResponseList responseArticle) {
		this.responseArticle = responseArticle;
	}

	
	
}
