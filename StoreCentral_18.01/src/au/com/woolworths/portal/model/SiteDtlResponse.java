package au.com.woolworths.portal.model;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class SiteDtlResponse {

	@JsonProperty("d")
	private SiteDtlResponseHelper siteDtlResponseHelper;

	/**
	 * @return the siteDtlResponseHelper
	 */
	public SiteDtlResponseHelper getSiteDtlResponseHelper() {
		return siteDtlResponseHelper;
	}

	/**
	 * @param siteDtlResponseHelper
	 *            the siteDtlResponseHelper to set
	 */
	public void setSiteDtlResponseHelper(
			SiteDtlResponseHelper siteDtlResponseHelper) {
		this.siteDtlResponseHelper = siteDtlResponseHelper;
	}

}
