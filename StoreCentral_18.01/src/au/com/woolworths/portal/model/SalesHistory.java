package au.com.woolworths.portal.model;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class SalesHistory {

	@JsonProperty("TotaledProperties")
	private String totaledProperties;

	@JsonProperty("A0WEEKDAY1_T")
	private String dayOfWeek;

	@JsonProperty("A0PLANT")
	private String storeNo;

	@JsonProperty("A0PLANT_T")
	private String storeName;

	@JsonProperty("A0MATERIAL")
	private String articleNo;

	@JsonProperty("A0MATERIAL_RORDMLTY")
	private String cartonQty;

	@JsonProperty("A8NWCRSGJXSOWYVZBBQTB6QWAP_F")
	private String salesQtyThisWeek;

	@JsonProperty("A8NWCRSGJXSOWYVZBBQTBJPY4H_F")
	private String salesQtyLastWeek;

	@JsonProperty("A8NWCRSGJXSOWYVZBBQTBVKM4H_F")
	private String salesQtyLastWeek1;

	@JsonProperty("A8NWCRSGJXSOWYVZBBQTC76I2P_F")
	private String salesQtyLastWeek2;

	@JsonProperty("A8NWCRSGJXSOWYVZBBQTCU794X_F")
	private String salesQty15WeekAvg;

	public String getDayOfWeek() {
		return dayOfWeek;
	}

	public void setDayOfWeek(String dayOfWeek) {
		this.dayOfWeek = dayOfWeek;
	}

	public String getStoreNo() {
		return storeNo;
	}

	public void setStoreNo(String storeNo) {
		this.storeNo = storeNo;
	}

	public String getStoreName() {
		return storeName;
	}

	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}

	public String getArticleNo() {
		return articleNo;
	}

	public void setArticleNo(String articleNo) {
		this.articleNo = articleNo;
	}

	public String getCartonQty() {
		return cartonQty;
	}

	public void setCartonQty(String cartonQty) {
		this.cartonQty = cartonQty;
	}

	public String getSalesQtyThisWeek() {
		return salesQtyThisWeek;
	}

	public void setSalesQtyThisWeek(String salesQtyThisWeek) {
		this.salesQtyThisWeek = salesQtyThisWeek;
	}

	public String getSalesQtyLastWeek() {
		return salesQtyLastWeek;
	}

	public void setSalesQtyLastWeek(String salesQtyLastWeek) {
		this.salesQtyLastWeek = salesQtyLastWeek;
	}

	public String getSalesQtyLastWeek1() {
		return salesQtyLastWeek1;
	}

	public void setSalesQtyLastWeek1(String salesQtyLastWeek1) {
		this.salesQtyLastWeek1 = salesQtyLastWeek1;
	}

	public String getSalesQtyLastWeek2() {
		return salesQtyLastWeek2;
	}

	public void setSalesQtyLastWeek2(String salesQtyLastWeek2) {
		this.salesQtyLastWeek2 = salesQtyLastWeek2;
	}

	public String getSalesQty15WeekAvg() {
		return salesQty15WeekAvg;
	}

	public void setSalesQty15WeekAvg(String salesQty15WeekAvg) {
		this.salesQty15WeekAvg = salesQty15WeekAvg;
	}

	public String getTotaledProperties() {
		return totaledProperties;
	}

	public void setTotaledProperties(String totaledProperties) {
		this.totaledProperties = totaledProperties;
	}

}
