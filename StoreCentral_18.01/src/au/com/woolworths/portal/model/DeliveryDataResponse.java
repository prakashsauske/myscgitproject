package au.com.woolworths.portal.model;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.annotate.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class DeliveryDataResponse {

	@JsonProperty("d")
	private DeliveryDataResponseHelper deliveryDataResponseHelper;

	/**
	 * @return the deliveryDataResponseHelper
	 */
	public DeliveryDataResponseHelper getDeliveryDataResponseHelper() {
		return deliveryDataResponseHelper;
	}

	/**
	 * @param deliveryDataResponseHelper
	 *            the deliveryDataResponseHelper to set
	 */
	public void setDeliveryDataResponseHelper(
			DeliveryDataResponseHelper deliveryDataResponseHelper) {
		this.deliveryDataResponseHelper = deliveryDataResponseHelper;
	}

}
